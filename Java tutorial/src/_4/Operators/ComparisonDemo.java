package _4.Operators;

public class ComparisonDemo {
	public static void main(String[] args) {
		int value1, value2;
		value1 = 1;
		value2 = 3;
		
		if(value1>value2) {
			System.out.println("value1 > value2");
			
		}
		
		if(value1>=value2) {
			System.out.println("value1 >= value2");
			
		}
		
		if(value1==value2) {
			System.out.println("value1 == value2");	
		}
		
		if(value1!=value2) {
			System.out.println("value1 != value2");	
		}
		
		if(value1<value2) {
			System.out.println("value1 < value2");
			
		}
		
		if(value1<=value2) {
			System.out.println("value1 <= value2");
			
		}
		
	}

}
