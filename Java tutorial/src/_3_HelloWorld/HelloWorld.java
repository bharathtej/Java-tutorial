package _3_HelloWorld;

public class HelloWorld {
	public static void main(String[] args) {
		/*"public" is used because java runtime has to find this method
		 * "Static is used to run a class without creating a object because of the constructor issues
		 * "Void" is return type of the method as java runtime not need any return value from the main method
		 * "main" is the exact method name 
		 * we use "String [] args because any command line argument is taken through this string array and variable name can be anything instead
		   of args.
		 */
		System.out.println("Hello World!");
		
	}

}
